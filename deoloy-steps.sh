########### DEPLOY APPLICATON IN GKE AUTOPOILOT MODE #######################

Create a Docker Repository in Artifact Registory

Build Docker Image using Dockerfile 

Push the Docker image to Artifact registory   # use cicd do push the image 

Create an Autopoilot Cluster in GKE using Terraform 

Deploy the application in GKE Cluster using CI/CD Github Action # modify the cicd github action file (YML) To deploy the app

Expose the App to the Internet



